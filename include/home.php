						<p style="text-align:left;">
						1.What is the Raw Material used for manufacture of cement?<br/>
						Ans:<span style="color:white;">Calicareous (Limestone Obtained from mines) ,argillaceous 
						materials (Clay, Bauxite) and Iron are the commonly used raw materials in the manufacture of cement.</span> <br/><br/>
						
						2.How is cement manufactured? <br/>
						Ans:<span style="color:white;">Manufacture of cement comprises of four stages viz. <br/>
						&nbsp;&nbsp;1. Extraction of lime stone from mines. <br/>
						&nbsp;&nbsp;2.Blending of ground limestone,Clay or Bauxite and iron ore or Laterite 
							in right proportion and cintering in rotary kilns at a high temperature of 1400oC to 1500oC form Clinker. <br/>
						&nbsp;&nbsp;3. Grinding of clinker with Gypsum to form cement. <br/>
						&nbsp;&nbsp;4. Storing in silos, testing and despatch form the final process of manufacture?</span><br/><br/>
						
						3.What is the chemical composition of cement and the contribution of each chemical compound?<br/>
