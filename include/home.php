						<p style="text-align:left;">
						
						4.What is the role of Gypsum in cement?<br/>
						Ans:<span style="color:white;">Gypsum is added to control the 'setting of cement'.</span><br/><br/>
						
						5.What is 'Setting of Cement'?<br/>
						Ans:<span style="color:white;">Setting is the stiffening of the cement paste.Broadly it refers to change of fluid to rigid state.</span><br/><br/>
						
						6.What do you mean by hardening of cement? <br/>
						Ans:<span style="color:white;">Hardening is the gain of strength in cement. This process of gain of 
						strength continuous up to one year rapidly approximately and gradually after one year and up to 5 years.</span> <br/><br/>
						
						7.Why should cement be 'fine'?<br/>
						Ans:<span style="color:white;">The rate of hydration of cement depends on the fineness of cement and for a rapid development of strength high fineness is necessary.</span><br/><br/>
						
						8.What is meant by 'Soundness of Cement'? <br/>
						Ans:<span style="color:white;">Cements may sometimes contain free lime, which may cause expansion. 
						Hence soundness test is conducted to see that even if uncombined lime
						is present in cement whether it is in prescribed limits. If it is within the prescribed limits then it is said to be sound.</span> <br/><br/>
						
						9.What does, 53 grade, 43 grade and 33 grade cement indicate?<br/>
						Ans:<span style="color:white;">Grades 53 , 43 and 33 indicate the compressive strength of cements in Newtons for one mm2 area. i.e.
						53 grade means a compressive strength of 53 N.per mm2 attained after a curing period of 28 days. 
						Similarly 43 and 33 grades may be defined. </span><br/><br/>
						
						10.Does the colour of the cement affect it's strength?<br/>
						Ans:<span style="color:white;">The colour of the cement depends on the raw mateirals used.
						If the raw materials contain more iron resulting cements will be darker.
						Colour has no significance on any properties of cement. 
						The strength of the cement is dependent on the correct proportion of raw material mix.</span> <br/><br/>
						
						11.What are the reasons for slow setting and quick setting of cement?<br/>
						Ans:<span style="color:white;">Slow setting in cement is due to Salts and chemicals in water, sand and aggregate. Cold Weather,
						Less cements , High % of impurities, adultrated cement, improper water cement ratio. 
						Quick setting of Cement is due to : addition of low quantity of Gypsum, hot weather condition, high cement fineness.</span><br/><br/>
						
						12.How to store cement?<br/>
						Ans:<span style="color:white;">Proper storage of cement shall permit easy access for inspection and identification.
						Cement shall be stored in suitable weather tight structures, to protect the cement from dampness.
						It shall not be piled more thah ten bags in a stack and shall be arranged in header and stretcher
						fashion as far as possible.while removing the bags for use,"FIRST IN FIRST OUT" rule shall be applied. </span><br/><br/>
						
						13.What is Mortar?<br/>
						Ans:<span style="color:white;">Mortar is a mixture of sand, cement and water.</span> <br/><br/>
						
						14.Why is concrete the most widely used construction material? <br/>
						Ans:<span style="color:white;">Concrete is the most versatile mouldable construction material ever known to mankind.
						It bears excellent fire and weather resistant properties. Most intricate shapes can be achieved with concrete .
						It is very good in compression though weak in tension.It is supplimented by steel to achieve tensile strength.
						Centuries old monuments bear testimony for its life.</span>  <br/><br/>
						
						15.What should be the quality of aggregate for a good concrete? <br/>
						Ans:<span style="color:white;">Aggregate for concrete should be inert, dense, hard durable 
						,structurally sound, capable of developing good bond with the cement paste,
						weather resistant and unaffected by water.</span>  <br/><br/>
						
						16.What is the sources of aggregate? <br/>
						Ans:<span style="color:white;">Natural sand, gravel, pebbles, rocks crushed to required size and manufactured 
						aggregates like blast furnace slag are sources of aggregate. IS 456-2000 stipulates
						that the aggregates should conform to IS 383-1970.</span>  <br/><br/>
						
						17.What is the function of water in 'concrete'? <br/>
						Ans:<span style="color:white;">It is with water that the cement undergoes the process of hydration to form a
						gel with binding property covering the aggregates uniformly on mixing. 
						However water for mixing shall be limited to achieve a required consistency as excess water is deterrent to concrete.</span>  <br/><br/>
						
						18.Can any water be used in concreting? <br/>
						Ans:<span style="color:white;">It is important that water used in concrete should be free from impurities Generally it should be of potable quality. 
						Bore water would need checking before using because it often contains impurities which affect setting times and reduce
						the strength of concrete. Sea water is not suitable for reinforced concrete as it causes corrosion. 
						IS456-1978 gives the maximum possible limits of solids in water.</span>  <br/><br/>
						
						19.What is an admixture? <br/>
						Ans:<span style="color:white;">An admixture is a chemical formulation generally added to concrete while mixing to induce certain desirable
						properties to it like better workability with water reducing admixture, delayed setting the retarders, 
						improve the rate of hardening with accelerating admixtures, and air entertainment for better flow and lubrication.</span>  <br/><br/>
						
						20.What is meant by 'Reinforced Cement Concrete' (RCC)? <br/>
						Ans:<span style="color:white;">Concrete is strong in compression and weak in tension.
						A plain concrete beam when it is subjected to bending fails at the bottom side due to tension. 
						Hence to make concrete strong in tension, steel is embedded so that it becomes strong both in tension and compression.
						The concrete embedded with steel is called `Reinforced Cement Concrete'.</span>  <br/><br/>
						
						21.What are the factors affecting the workability of concrete?  <br/>
						Ans:<span style="color:white;">The factors that are affecting the workability of concrete are water content, 
						aggregate type and grading, aggregate/cement ratio, presence of admixtures and fineness of cement. </span> <br/><br/>
						
						22.What is meant by batching? <br/>
						Ans:<span style="color:white;">Measurement of various ingredients of concrete like sand, metal and cement for each batch of concrete per design is called batching. 
						Volume batching and weigh batching are the two types of batching in vogue.</span> <br/><br/>
						
						23.What is the process of mixing of concrete? <br/>
						Ans:<span style="color:white;">The different stages of `mixing of concrete' are : 
						To locate a correct spot on a firm ground close to the concrete site,
						positioning the mixture machine conveniently oriented to feed the hopper , 
						emptying the drum of concrete on the platform prepared. To stack metal,sand 
						and cement per design mix work out per batch.to feed the drum with required 
						quantity of water.To feed the drum with the contents of hopper and allow the
						mix in the rotating drum for 1 &1/2 to 2 minutes till a homogeneous mix is obtained .
						To empty the drum of concrete on the platform and leading it to the concreting spot.</span>  <br/><br/>
						
						24.What are the factors affecting the strength of concrete? <br/>
						Ans:<span style="color:white;">The factors that are affecting strength of concrete are : Water cement ratio Shape and size of aggregate Aggregate cement ratio 
						,Degree of compaction ,Age of concrete and Air entrainment and moisture in aggregate.</span>  <br/><br/>
						
						25.What is meant by seggregation of concrete? <br/>
						Ans:<span style="color:white;">Seggregation of concrete can occur in two form. In the first the coarser particles tend
						to separate out while concrete being conveyed along a slope(for e.g.sloped roof and staircase waist slab)
						or,not being homogeneous coarse aggregate settle below leaving the slurry with fine aggregate below.
						The second form of seggregation occurs in wet mixes by separation of grout from the mix.
						This happens due to over vibration of concrete and dropping of concrete from a height .</span>  <br/><br/>
						
						26.Why is a 'crack' developed? <br/>
						Ans:<span style="color:white;">A crack is developed whenever stresses in the structural component exceeds its strength .
						This may be due to external forces, foundation settlement, thermal changes, chemical action etc. </span> <br/><br/>
						
						27.Why do 'plastering cracks' occur?  <br/>
						Ans:<span style="color:white;">Plastering cracks appear because of evaporation of water, increased thickness, very fine sand, 
						low quality bricks, non uniform plastering, improper seasoning of walls, use of different mix proportion , 
						improper filling of joints, insufficient curing, more free lime/MgO. </span> <br/><br/>
						
						28.Why do RCC cracks? <br/>
						Ans:<span style="color:white;">RCC cracks due to insufficient reinforcement and cover, corroded steel, high slump, improper vibration,
						volume changes, excessive coarse aggregates, expansion of concrete, lack of sufficient quantity of cement, 
						electrical conduits not placed at sufficient depth in concrete. </span> <br/><br/>
						
						29.What do you mean by M20, M30, M40 concrete? <br/>
						Ans:<span style="color:white;">M20, M30, M40 Grades of concrete indicates compressive strength of concrete in Newtons for one mm2 area 
						which is called the characteristic strength of concrete after 28 days. M20 mean 20 N/mm2 similarly M30 is 30 N/mm2.</span>  <br/><br/>
						
						30.What is meant by water cement ratio? <br/>
						Ans:<span style="color:white;">The quantity of water used for mixing of concrete per unit volume related to the quantity of cement is called 
						water cement ratio and is expressed as the ratio of the weight of water to the weight of cement used in a unit volume of concrete.</span>  <br/><br/>
						
						31.What is curing? <br/>
						Ans:<span style="color:white;">Maintenance of a favourable environment for the continuation of chemical reaction that are responsible for hardening of cement is called curing.</span><br/><br/>
						
						32.What methods are commonly employed to ensure sufficient moisture for curing?  <br/>
						Ans:<span style="color:white;">Ponding, continuous sprinkling, covering with wet cloth, polyethylene sheets, sealing coat applied as a
						liquid commonly known as "Curing compounds" are some of the methods commonly employed for curing. </span> <br/><br/>
						
						33.What are the common mistakes which affect the quality of concrete?  <br/>
						Ans:<span style="color:white;">Use of too much or too little water for mixing. <br/>
						&nbsp;&nbsp;Incomplete mixing of aggregate with cement.<br/>
						&nbsp;&nbsp;Improper grading of aggregates resulting in seggregation or blending of concrete.<br/>
						&nbsp;&nbsp;Inadequate compaction of concrete.<br/>
						&nbsp;&nbsp;Using concrete which has already begun to set.<br/>
						&nbsp;&nbsp;Placing of concrete on a dry foundation without properly wetting it with water.<br/>
						&nbsp;&nbsp;Use of aggregate containing foreign materials such as earthy matters, clay or lime. Use of water containing solids, chemicals etc<br/>
						&nbsp;&nbsp;Too much trowelling of the concrete surface.<br/>
						&nbsp;&nbsp;Leaving the finished concrete surface exposed to sun and wind during the first ten days after placing without curing.</span> <br/><br/>
